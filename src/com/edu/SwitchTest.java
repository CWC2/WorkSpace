package com.edu;

public class SwitchTest {

	public static void main(String[] args) {
		int x = 1;
		switch(x) {
		case 1 :
			System.out.println("This is 1");
			break;
		case 2 :
			System.out.println("This is 2");
			break;
		default:
			System.out.println("No");
			break;
		}
		System.out.print("Hello World");
	}

}
